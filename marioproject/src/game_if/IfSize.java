package game_if;

public class IfSize {

	public double width;
	public double height;
	
	public IfSize(double width, double height) {
		super();
		this.width = width;
		this.height = height;
	}
	
	public IfSize(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}
	
	
}
